#!/bin/bash
FINAL_PENDING_ETH_WEI=5491413293384645
FINAL_PENDING_BEAN_WEI=6024800953015380
CLAIM_THRESHOLD_ETH=${CLAIM_THRESHOLD_ETH:-10000000000000000}
CLAIM_THRESHOLD_BEAN=${CLAIM_THRESHOLD_BEAN:-1000000000000000000}

echo "PENDING_ETH_WEI=$FINAL_PENDING_ETH_WEI"
echo "PENDING_BEAN_WEI=$FINAL_PENDING_BEAN_WEI"
echo "THRESHOLD_ETH=$CLAIM_THRESHOLD_ETH"
echo "THRESHOLD_BEAN=$CLAIM_THRESHOLD_BEAN"

claim_eth=0
claim_bean=0

if [ "$FINAL_PENDING_ETH_WEI" -ge "$CLAIM_THRESHOLD_ETH" ] && [ "$CLAIM_THRESHOLD_ETH" -gt 0 ]; then
  claim_eth=1
fi

if [ "$FINAL_PENDING_BEAN_WEI" -ge "$CLAIM_THRESHOLD_BEAN" ] && [ "$CLAIM_THRESHOLD_BEAN" -gt 0 ]; then
  claim_bean=1
fi

echo "CLAIM_ETH=$claim_eth"
echo "CLAIM_BEAN=$claim_bean"
